@app.route('/venues')
def venues():
  # TODO: replace with real venues data.
  #       num_shows should be aggregated based on number of upcoming shows per venue.
  data = []
  city = []
  state = []
  numUpShow = 0
  venues = Venue.query.order_by('id').all()

  for venue in venues:
    venue_data = {
      "city": venue.city,
      "state": venue.state,
      "venues":[]
    }
    if venue.city not in city and venue.state not in state:
      city.append(venue.city)
      state.append(venue.state)
      data.append(venue_data)
  
  for i in venues:
    shows = Show.query.filter_by(venue_id=i.id).all()
    for show in shows:
      if show.start_time < datetime.now():
        numUpShow += 1
    for v in data:
      if i.city == v["city"] and i.state == v["state"]:
        v["venues"].append({
          "id": i.id,
          "name": i.name,
          "num_upcoming_shows": numUpShow
        })

  return render_template('pages/venues.html', areas=data);

